words = {}

counter = 0

for txt in wiki:
    doc = nlp(txt)
    counter += 1
    print("Analiza słów, krok " + str(counter) + " z " + str(words_limit))
    for token in doc:
        if not token.is_stop and not token.is_punct and not token.is_digit and token.is_oov == False:
            word = token.text.strip()
            lemma = token.lemma_.strip()
            
            if not lemma or lemma[0].isupper():
                continue

            if token.pos_ == "VERB":
                if lemma not in words:
                    words[lemma] = word.lower()

            if token.pos_ == "NOUN":
                if lemma not in words:
                    words[lemma] = word.lower()

            if token.pos_ == "ADJ":
                if lemma not in words:
                    words[lemma] = word.lower()

    if counter > words_limit:
        break


print(words)

