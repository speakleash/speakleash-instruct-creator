    output = random.choice(["Nie", "nie",f"Słowo: {words[k]} nie jest wulgaryzmem", f"Słowo: {words[k]} nie jest wulgarne", f"Słowo: {words[k]} nie jest przekleństwem", f"Słowo: {words[k]} nie jest obraźliwe", f"Słowo: {words[k]} nie jest nieodpowiednie", f"Słowo: {words[k]} nie jest niestosowne", f"Słowo: {words[k]} nie jest wulgarnym wyrażeniem", f"Słowo: {words[k]} nie jest nieprzyzwoite"])

